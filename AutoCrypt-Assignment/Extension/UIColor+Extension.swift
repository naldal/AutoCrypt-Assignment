import UIKit

extension UIColor {
    
    /// red: 30.0, green: 32.0, blue: 30.0, alpha: 1.0
    @nonobjc class var charcoal: UIColor {
        return UIColor(red: 30.0/255.0, green: 32.0/255.0, blue: 30.0/255.0, alpha: 1.0)
    }
    
    /// red: 234.0, green: 235.0, blue: 234.0, alpha: 1.0
    @nonobjc class var ice: UIColor {
        return UIColor(red: 234.0/255.0, green: 235.0/255.0, blue: 234.0/255.0, alpha: 1.0)
    }

    /// red: 7.0, green: 227.0, blue: 114.0, alpha: 1.0
    @nonobjc class var tealishGreen: UIColor {
        return UIColor(red: 7.0/255.0, green: 227.0/255.0, blue: 114.0/255.0, alpha: 1.0)
    }
    
    /// red: 1.0, green: 60.0, blue: 0.0, alpha: 1.0
    @nonobjc class var redOrange: UIColor {
        return UIColor(red: 1.0, green: 60.0 / 255.0, blue: 0.0, alpha: 1.0)
    }
    
    /// red: 154.0, green: 160.0, blue: 154.0, alpha: 1.0
    @nonobjc class var greenishGrey: UIColor {
        return UIColor(red: 154.0/255.0, green: 160.0/255.0, blue: 154.0/255.0, alpha: 1.0)
    }
    
    /// red: 243.0, green: 243.0, blue: 243.0, alpha: 1.0
    @nonobjc class var cancelGray: UIColor {
        return UIColor(red: 243.0/255.0, green: 243.0/255.0, blue: 243.0/255.0, alpha: 1.0)
    }
    
    /// red: 243.0, green: 88.0, blue: 93.0, alpha: 1.0
    @nonobjc class var gunmetal: UIColor {
        return UIColor(red: 88.0 / 255.0, green: 93.0 / 255.0, blue: 88.0 / 255.0, alpha: 1.0)
    }
    
    /// red: 35.0, green: 82.0, blue: 240.0, alpha: 1.0
    @nonobjc class var azul: UIColor {
        return UIColor(red: 35.0 / 255.0, green: 82.0 / 255.0, blue: 240.0 / 255.0, alpha: 1.0)
    }
    
    /// red: 77.0, green: 150.0, blue: 255.0, alpha: 1.0
    @nonobjc class var pastelBlue: UIColor {
        return UIColor(red: 77.0 / 255.0, green: 150.0 / 255.0, blue: 255.0 / 255.0, alpha: 1.0)
    }
    
    /// red: 255.0, green: 107.0, blue: 107.0, alpha: 1.0
    @nonobjc class var pastelRed: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 107.0 / 255.0, blue: 107.0 / 255.0, alpha: 1.0)
    }
    
    /// white: 249.0
    @nonobjc class var whiteF9: UIColor {
        return UIColor(white: 249.0 / 255.0, alpha: 1.0)
    }
    
    /// white: 243.0
    @nonobjc class var whiteF3: UIColor {
        return UIColor(white: 243.0 / 255.0, alpha: 1.0)
    }
    
    /// red: 0.0, green: 0.0, blue: 0.0, alpha: 0.05
    @nonobjc class var shadowBlack: UIColor {
        return UIColor(red: 0.0/255.0, green: 0.0/255.0, blue: 0.0/255.0, alpha: 0.05)
    }
}
